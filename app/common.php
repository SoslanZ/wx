       return  base64_encode(base64_encode(urlencode(json_encode($arr)))).'+';
    }
    if($type=='decode'){
        $newstr = substr($arr,0,strlen($arr)-1);
        return json_decode(urldecode(base64_decode(base64_decode($newstr))),1);
